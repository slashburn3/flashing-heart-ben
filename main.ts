function on_forever() {
    basic.showIcon(IconNames.Heart)
    basic.clearScreen()
    basic.pause(500)
    basic.showIcon(IconNames.Heart)
    basic.clearScreen()
    basic.pause(500)
    basic.showIcon(IconNames.Heart)
    basic.clearScreen()
    basic.pause(500)
}

while (true) {
    on_forever()
}
